namespace Runtime;

public enum ValueType
{
    /// <summary>
    /// Значение отсутствует.
    /// </summary>
    Void,

    /// <summary>
    /// Значение с плавающей запятой.
    /// </summary>
    Integer,

    /// <summary>
    /// Значение с плавающей запятой.
    /// </summary>
    Float,

    /// <summary>
    /// Строковое значение.
    /// </summary>
    String,
}